<h4  class="text" style=" font: 1.2em 'droid_sansregular',Arial;font-weight:bold; margin-top:10px;">Pre-Conditions/Restrictions:-</h4>

<ul>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">(i) Passengers must be holding a Business Class / Economy Class / Flying Returns / Frequent Flyer Programme of Star Alliance Partners ticket.</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">(ii) Upgrade permitted only to the next Class. Economy to First is not permitted;</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">(iii) The passenger should be ticketed with a confirmed booking in lower class to be eligible for this scheme;</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">(iv) No other schemes can be combined with Airport Upgrade scheme. e.g. Companion Scheme with Airport Upgrade scheme;</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">(v) Lounge Facility at the Airport is permitted for this scheme;</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">(vi) No child and infant Discount Permitted on the Airport Upgrade scheme;</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">(vii) Airport Upgrade amounts ex-India are inclusive of Service Tax.</li>
</ul>


<h4 class="head">Modalities</h4>

<ul>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">1. Passengers can pay the Airport Upgrade amount at the airport or Air India City offices by purchasing an EMD in advance across the country.</li>
<li  class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">2. The EMD can be redeemed against the Airport Upgrade if seats available on the Gateway point to Destination; and Upgrade will be granted from Gateway point to Destination.</li>
<li  class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">3. The EMDs will be used at the airport (e.g. BOM-DEL-JFK) when the upgrade materializes from the mentioned gateway points on the route.</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">4. If passenger is purchasing an EMD in advance, the passenger must be listed on SA basis in Executive Class against RBD Z. This will help the Airports in arranging meals for the higher class.</li>
<li  class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">5. Such EMDs can be fully refunded if unutilized against Airport Upgrade.</li>
</ul>


<p class="text"><span style="font-weight:bold ;">Baggage Allowance:-</span> Baggage Allowance will be as per the original booking of the passenger. No enhanced baggage allowance of the higher class of travel is permitted.</p>

<p class="text"><span style="font-weight:bold">FFP Miles:-</span> FFP Miles will be as per original class of travel. - e.g. Passengers holding Economy Class ticket availing of Airport Upgrade will get FFP accrual of miles of Economy Class;</p>

<h4 class="head">Applicability on through Flight Leg:-</h4>


<ul>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">i) When Passengers are through checked in with Immigration / Customs cleared at Boarding Point, but the flight number changes, (i.e. AI 011/AI101 AMD-DEL-JFK), if Passenger is through checked from AMD to JFK, Airport Upgrade Scheme will be available only
 for the DEL JFK leg.</li>
 <li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">ii) However, if passengers are travelling on AI 101 Ex BOM, e.g. BOM-DEL-JFK, the passenger can avail of the Airport Upgrade Scheme for the entire journey from BOM as it is the same flight number.</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">iii) If the passenger is travelling NAG-DEL-JFK/CHI, the passenger will have to pay separately for NAG-DEL Leg Airport Upgrade, and for the DEL-JFK/CHI Leg Airport Upgrade.</li>
