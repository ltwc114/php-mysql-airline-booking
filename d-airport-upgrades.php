 offer. </li>
 <li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">Passengers availing airport upgrade scheme will be permitted economy class baggage allowance.</li>
 <li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">No advance vouchers, in any form, can be purchased either from the city office or agency.</li>
 <li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">Lounge facility is extended to passengers availing this offer. </li>
