<p class="text" style="margin-top:20px;">Here is a chance for everyone to fly Business class on Air India domestic routes!</p>


<p class="text">The luxury of sitting in the front cabin at an extremely affordable price is possible, every time one boards a flight when flying to a domestic destination on an aircraft that offers Business Class Service.</p>



<p class="text" style="font-weight:bold">The slient features of the Domestic Upgrade scheme are listed below:</p>



<ul >
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">'Get Up Front' offer has two payment slabs - Rs.4500 for distances up to 750 Km and Rs.6000 for higher distances to avail the upgrade. This is inclusive of Service Tax and Swachh Bharat Cess.</li>
<li class="text"  style="background:url(images/sprite.png) no-repeat 4px 5px;">Since Infant is not provided any seat in the aircraft, NIL amount to be collected from the Infant.</li>
<li class="text" style="background:url(images/sprite.png) no-repeat 4px 5px;">The upgrade at the airport is subject to availability of Business class seats and on First Come First Served Basis with physical presence of the passenger at the check-in counter.</li>
